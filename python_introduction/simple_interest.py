# simple_interest.py
# Calculate simple interest using I = P * R * T

# Given values
principal = 1000     # P = $1000
rate = 0.05          # R = 5% per year (as a decimal)
time = 3             # T = 3 years

# Calculation
interest = principal * rate * time

# Output
print(f"The simple interest is: {interest}")
